package doctorProject;

public class DoctorRegisterClass {
	private String firstname,lastname,phone,nationality,gender,title,department,email,location,username,password,picture;

	public DoctorRegisterClass(String firstname, String lastname, String phone, String nationality, String gender,
			String title, String department, String email,String location, String username, String password, String picture) {
		super();
		this.firstname = firstname;
		this.lastname = lastname;
		this.phone = phone;
		this.nationality = nationality;
		this.gender = gender;
		this.title = title;
		this.department = department;
		this.email = email;
		this.location = location;
		this.username = username;
		this.password = password;
		this.picture = picture;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getNationality() {
		return nationality;
	}

	public void setNationality(String nationality) {
		this.nationality = nationality;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPicture() {
		return picture;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}
	
	

}
